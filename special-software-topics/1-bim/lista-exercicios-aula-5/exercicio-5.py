#5.  Calcular  o  consumo  por  litro  de  um  carro,
#sendo  que  será  informada  a quantidade de quilômetros
#percorrido e a quantidade de litros abastecido. 
Quilometros = int(input("Insira o valor dos Quilometros percorridos: " ))
Litros = int(input("Insira o valor da quantidade de litros reabastecidos: " ))
resul = Quilometros / Litros
print("Valor do consumo por litro: "+ str(resul))
